import importlib.util
import os
import shutil
import subprocess
from pathlib import Path


def load_renderer():
    module_path = Path(__file__).resolve().parents[1] / "scripts" / "render_prompts.py"
    spec = importlib.util.spec_from_file_location("render_prompts", module_path)
    module = importlib.util.module_from_spec(spec)
    assert spec.loader is not None
    spec.loader.exec_module(module)
    return module


def test_render_document_places_harness_fragment_before_core():
    renderer = load_renderer()

    rendered = renderer.render_document(
        harness="codex",
        fragment="## Codex\ncodex body\n",
        core="# Global\ncore body\n",
        stamp="2026-04-25",
    )

    assert rendered.startswith("<!-- Generated by sync-ai-prompts on 2026-04-25.")
    assert rendered.index("codex body") < rendered.index("core body")
    assert "Edit prompts/core.md and prompts/harnesses/*.md" in rendered
    assert "/home/id" not in rendered


def test_render_document_places_private_overlay_after_core():
    renderer = load_renderer()

    rendered = renderer.render_document(
        harness="claude",
        fragment="## Claude\nclaude body\n",
        core="# Global\ncore body\n",
        private="## Private\nprivate body\n",
        stamp="2026-04-25",
    )

    assert rendered.index("claude body") < rendered.index("core body")
    assert rendered.index("core body") < rendered.index("private body")
    assert "private.example.md" in rendered


