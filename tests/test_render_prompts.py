def test_target_path_uses_home_and_env_override(tmp_path):
    renderer = load_renderer()

    default_path = renderer.target_path("codex", home=tmp_path, env={})
    override_path = renderer.target_path(
        "codex",
        home=tmp_path,
        env={"CODEX_AGENTS_PATH": "/custom/AGENTS.md"},
    )

    assert default_path == tmp_path / "AGENTS.md"
    assert override_path == Path("/custom/AGENTS.md")


def test_render_all_writes_selected_harnesses_to_output_dir(tmp_path):
    renderer = load_renderer()
    repo = tmp_path / "repo"
    (repo / "prompts" / "harnesses").mkdir(parents=True)
    (repo / "prompts" / "core.md").write_text("# Core\nshared\n", encoding="utf-8")
    (repo / "prompts" / "private.md").write_text("## Private\nlocal only\n", encoding="utf-8")
    (repo / "prompts" / "harnesses" / "claude.md").write_text(
        "## Claude\nclaude only\n",
        encoding="utf-8",
    )
    (repo / "prompts" / "harnesses" / "codex.md").write_text(
        "## Codex\ncodex only\n",
        encoding="utf-8",
    )

    written = renderer.render_all(
        repo_root=repo,
        out_dir=tmp_path / "out",
        selected=["claude", "codex"],
        stamp="2026-04-25",
    )

    assert written == {
        "claude": tmp_path / "out" / "CLAUDE.md",
        "codex": tmp_path / "out" / "AGENTS.md",
    }
    assert "claude only" in written["claude"].read_text(encoding="utf-8")
    assert "shared" in written["codex"].read_text(encoding="utf-8")
    assert "local only" in written["codex"].read_text(encoding="utf-8")


